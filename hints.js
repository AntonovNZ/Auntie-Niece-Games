function showHint() {
    const hints = [
      "The country is located in the " + countries[answerIndex].hemisphere + " hemisphere.",
      "The calling code for this country is +" + countries[answerIndex].calling_code + ".",
      "The country is located in " + countries[answerIndex].continent + ".",
    ];

    let populationHint = "";
    if (countries[answerIndex].population > countries[compareIndex1].population) {
      populationHint = "The population is larger than that of " + compareCountry1 + ".";
    } else if (countries[answerIndex].population < countries[compareIndex1].population) {
      populationHint = "The population is smaller than that of " + compareCountry1 + ".";
    } else {
      populationHint = "The population is equal to that of " + compareCountry1 + ".";
    }
    hints.push(populationHint);

    let sizeHint = "";
    if (countries[answerIndex].size > countries[compareIndex2].size) {
      sizeHint = "The country is larger than " + compareCountry2 + ".";
    } 
    else if (countries[answerIndex].size < countries[compareIndex2].size) {
      sizeHint = "The country is smaller than " + compareCountry2 + ".";
    } 
    else {
      sizeHint = "The country is equal in size as " + compareCountry2 + ".";
    }
    hints.push(sizeHint);

    hints.push("The country borders " + randomBorder + ".");

    hints.push(
      "The flag of the country has the following colors: " + countries[answerIndex].flag_colours.join(", ") + ".",
      "The country starts with the letter '" + countries[answerIndex].name.charAt(0).toUpperCase() + "'."
    );

    if (countries[answerIndex].capital != null) {
      hints.push("The capital city is " + countries[answerIndex].capital + ".");
    } 
    else {
      hints.push("The country does not have an official capital.");
    }

    hints.push(
      "The official language(s) spoken is/are " + countries[answerIndex].languages.join(", ") + ".",
      "The currency used is " + countries[answerIndex].currency + ".",
      "The internet TLD for this country is " + countries[answerIndex].internet_tld + "."
    );

    if (hintCounter < hints.length) {
      document.getElementById("hints").innerHTML += hints[hintCounter] + "<br>";
      hintCounter++;
    } 
    else {
      streak = 0;
      document.getElementById("streak").textContent = "Streak: " + streak;
      document.getElementById("answer").textContent = "The country was " + countries[answerIndex].name + ".";
      document.getElementById("hints").textContent = "";
      document.getElementById("result").textContent = "";
      document.getElementById("submitGuess").textContent = "Next Country";
    }
  
}

function submit() {
  guess = document.getElementById("countryDropdown").value;
  document.getElementById("countryDropdown").value = "";
  let capitalisedGuess = capitaliseFirstLetter(guess);
  if (guess != ""){
    if (guess.toLowerCase() === countries[answerIndex].name.toLowerCase()) {
      streak++;
      document.getElementById("result").innerHTML = "Correct! The country is " + countries[answerIndex].name + ".";
      document.getElementById("streak").textContent = "Streak: " + streak;
      document.getElementById("giveUpButton").style.display = "none";
      document.getElementById("submitGuess").style.display = "none";
      document.getElementById("nextCountryButton").style.display = "block";
    } 
    else {
      document.getElementById("result").innerHTML += capitalisedGuess + " is incorrect. Try again!" + "<br>";
      showHint();
    }
  }
}

function giveUp() {
  hintCounter = 0;
  streak = 0;
  document.getElementById("giveUpButton").style.display = "none";
  document.getElementById("hints").style.display = "none";
  document.getElementById("result").innerHTML = "The country was " + countries[answerIndex].name + ".";
  document.getElementById("submitGuess").style.display = "none";
  document.getElementById("guessTitle").style.display = "none";
  document.getElementById("hintTitle").style.display = "none";
  document.getElementById("nextCountryButton").style.display = "block";
  document.getElementById("streak").textContent = "Streak: " + streak;
}

